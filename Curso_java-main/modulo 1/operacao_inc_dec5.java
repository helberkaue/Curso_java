public class operacao_inc_dec5 {
    public static void main(String[] args) {
        int num = 10;
        System.out.println(num);
        num = ++num;
        System.out.printf("Número após o incremento: %d\n",num);
        num = --num;
        System.out.printf("Número após o decremento: %d",num);

    }
}
